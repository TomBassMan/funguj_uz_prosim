package cz.klimes;

public class BarvaFigurky {
    private int start;
    private int cil;
    public BarvaFigurky(int start, int cil){
        this.start = start;
        this.cil = cil;
    }

    public int getStart() {
        return start;
    }

    public int getCil() {
        return cil;
    }
}
